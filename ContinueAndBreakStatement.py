'''
Continue and Break statement
        * continue keyword is used to skip the code return below continue keyword
        * break keyword is used to break the loop
'''

# continue
for val in 'string':
    if val == 'i':
        continue
    print(val)
print('end loop')

# break
for val in 'string':
    if val == 'i':
        break
    print(val)
print('end loop')

